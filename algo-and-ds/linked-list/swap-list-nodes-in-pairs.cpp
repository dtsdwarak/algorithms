ListNode* swapPairs(ListNode *A) {
    if (A == NULL || A->next == NULL)
        return A;
        
    ListNode *head = NULL, *prev_back = NULL;
        
    while(A != NULL && A->next != NULL) {
        cout<<"\n Inga varen . A = "<<A->val<<"\n";
        ListNode *backup = A->next;
        A->next = A->next->next;
        backup->next = A;
        if (!head)
            head = backup;
        if (prev_back)
            prev_back->next = backup;
        prev_back = A;
        A = A->next;
    }
    
    return head;
}


